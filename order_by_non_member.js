/**
 * 指定した数字をランダムに取得します
 */
function getRandomNumber(min, max) {
  return Math.floor(Math.random() * (max + 1 - min)) + min;
}

/**
 * メールアドレスをランダムに取得します
 */
function getMailSuffix() {
  let date = new Date();
  
  return '+' + date.getMonth() + date.getDate() + date.getMilliseconds();
}


