/**
 * コンボボックスがある場合に、ランダムに選択します
 */
var selectCombo = function* (nightmare, selector) {
  let select = '';
  yield nightmare
    .evaluate(selector => {
      let vals = [];
      let options = document.querySelector(selector);
      if (options) {
        vals = Array.from(options).map(option => option.value);
      }
      return vals;
    }, selector)
    .then((vals) => {
      if (vals) {
        select = vals[getRandomNumber(1, vals.length - 1)];
      }
    });
  if (select) {
    yield nightmare
    .select(selector, select)
    .wait(1000);
  }
}

